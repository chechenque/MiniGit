package kass.concurrente.util;

public class Aritmetica {

    /**
     * Metodo que suma dos numeros y regresa dicha suma
     * @param x Primer numero
     * @param y Segundo numero
     * @return La suma de estos 2
     */
    public double suma(double x, double y) {
        return x + y + x;
    }

    /**
     * Metodo que multtiplica dos numeros y regresa dicha suma
     * @param x Primer numero
     * @param y Segundo numero
     * @return La multiplicacion de estos 2
     */
    public double multiplica(double x, double y) {
        return x * y;
    }
    
}
